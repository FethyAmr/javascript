var month = "January";
switch (month) {
    case "Winter": 
        console.log("Let it snow !");
        break;
    case "Spring": 
        console.log("Flowers everywhere !");
        break;
    case "Summer": 
        console.log("Let's go to the beach");
        break;
    case "Fall": 
        console.log("Let it snow !");
        break;
    default: 
        console.log("That's not a season...");
}