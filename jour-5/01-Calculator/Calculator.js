function calculator(Int1, operator, Int2){

    if(operator === "+"){
        console.log(Int1 + Int2);
        
    }
    if(operator === "-"){
        console.log(Int1 - Int2);
        
    }
    if(operator === "x"){
        console.log(Int1 * Int2);
        
    }
    if(operator === "/"){
        console.log(Int1 / Int2);
        
    }

}
calculator(4,"x",5)